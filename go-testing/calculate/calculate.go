package calculate

// Add function takes in two integers and sum the two values and returns an integer sum
func Add(a int, b int) int {
	return a + b
}

// Substract function takes in
func Substract(a int, b int) int {
	return a - b
}

// Multiply function that takes in two integers and multiply them and returns an integer.
func Multiply(a int, b int) int {
	return a * b
}
